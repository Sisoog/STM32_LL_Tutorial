/* read_uart function puts the data read(in USART interrupt) in a character variable */
	char read_uart(void)
	{
		char data = 0;
		while(!counter)
			__NOP();
		
		data = buffer[r_index++];
		if (r_index >= sizeof(buffer))
		{
				r_index=0;
		}
		
	//	__disable_irq();
		counter--;
	//	__enable_irq();
		
		return data;
	}
